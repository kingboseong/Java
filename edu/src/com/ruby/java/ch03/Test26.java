package com.ruby.java.ch03;

public class Test26 {

	public static void main(String[] args) {
		
		//for
		for(int i =10; i<10; i++) {
			System.out.println("for OK");
		}
		
		//while 
		int j = 0;
		while(j<10) {
			System.out.println("while OK");
			j++;
		}
		//do~while
		int k = 10;
		do {
			System.out.println("do-while OK");				
			k++;
		}while(k<10);
	}
}
